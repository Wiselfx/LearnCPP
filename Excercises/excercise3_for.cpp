#include <iostream>

int main() {
    int numb = 10;
    /* use the for loop to rewrite the countdown
     * for loop has three parts init where variable is initialised
     * then a condition and after that an expression which is always executed after the body*/
    for (int cdwn = 10; cdwn > -1; --cdwn) {
        std::cout << cdwn << std::endl;
    }
}